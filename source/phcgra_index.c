/* Form fan-out table — species with multiple sprite variants beyond the
 * (species, sex) pair. Entries here override the (species, sex) lookup.
 *
 * Slot 0 in `atlas_base` is the base/normal form; out-of-range forms
 * clamp to 0. To support a new multi-form species, just append a row —
 * no other code change needed.
 *
 * Convention for `form_count`: count of valid forms starting at 0
 * (Burmy plant/sandy/trash → 3, Arceus 18 plates → 18, etc.). */
typedef struct {
    uint16_t species;
    uint16_t atlas_base;
    uint8_t  form_count;
} phcgra_form_entry_t;

static const phcgra_form_entry_t kFormTable[] = {
    { 172, 662,  2 },  /* Pichu:    regular, spiky-eared */
    { 201, 590, 28 },  /* Unown:    A..Z, !, ? */
    { 351, 618,  4 },  /* Castform: normal, sun, rain, snow */
    { 386, 586,  4 },  /* Deoxys:   normal, attack, defense, speed */
    { 412, 622,  3 },  /* Burmy:    plant, sandy, trash */
    { 413, 625,  3 },  /* Wormadam: plant, sandy, trash */
    { 421, 632,  2 },  /* Cherrim:  overcast, sunshine */
    { 422, 628,  2 },  /* Shellos:  west, east */
    { 423, 630,  2 },  /* Gastrodon: west, east */
    { 479, 654,  6 },  /* Rotom:    normal, heat, wash, frost, fan, mow */
    { 487, 660,  2 },  /* Giratina: altered, origin */
    { 492, 652,  2 },  /* Shaymin:  land, sky */
    { 493, 634, 18 },  /* Arceus:   18 type plates */
};

uint16_t phcgra_index_from_species(uint16_t species, uint8_t sex, uint8_t form) {
    for (size_t i = 0; i < sizeof(kFormTable) / sizeof(kFormTable[0]); i++) {
        if (kFormTable[i].species == species) {
            uint8_t f = form < kFormTable[i].form_count ? form : 0;
            return (uint16_t)(kFormTable[i].atlas_base + f);
        }
    }
    if (species >= PHCGRA_SEX_TABLE_LEN) return 0xFFFF;
    return kSexTable[species][sex ? 1 : 0];
}
